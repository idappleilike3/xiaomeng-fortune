// 主 API: 根據牌 + 主題,生成完整解讀
function generateReading(cardIndex, suit, rank, theme) {
  theme = theme || "love";
  const suitMap = { wands: 0, cups: 1, swords: 2, pentacles: 3 };
  const suitKey = ["wands", "cups", "swords", "pentacles"][suitMap[suit]];

  let cardName, cardData;
  if (suit === "major") {
    cardData = MAJOR_ARCANA[cardIndex] || MAJOR_ARCANA[0];
    cardName = `${cardData.name}(${cardData.en})`;
  } else {
    const suitData = MINOR_ARCANA_GENERIC[suitKey] || MINOR_ARCANA_GENERIC.wands;
    cardName = `${suitData.name}・${rank}`;
    cardData = { themes: suitData.themes };
  }

  const templates = (cardData.themes && cardData.themes[theme]) || (cardData.themes && cardData.themes.love) || ["這張牌正在為你帶來重要的訊息。請靜心感受。"];
  const opener = FORTUNE_OPENERS[Math.floor(Math.random() * FORTUNE_OPENERS.length)];
  const body = templates[Math.floor(Math.random() * templates.length)];
  const closer = FORTUNE_CLOSERS[Math.floor(Math.random() * FORTUNE_CLOSERS.length)];

  return `${opener}\n\n${body}\n\n${closer}`;
}

window.__divinationSystem = { generateReading, MAJOR_ARCANA, MINOR_ARCANA_GENERIC };
